#basic 2-column keyed transpositional cipher
def encrypt_da_plain(plaintext):

    text = plaintext.replace(" ","").lower()
    column1 = ""
    column2 = ""

    for i in range(len(text)):
        if i % 2 == 0:
            column1 = column1 + text[i]
        else: 
            column2 = column2 + text[i]

    encryptText = column1 + column2 
    return encryptText

